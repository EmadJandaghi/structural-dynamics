          0,0,0,dNxy(1,3),-NV(3),0,0,0,dNxy(1,4),-NV(4),0,0,0,...
          dNxy(1,5),-NV(5),0,0,0,dNxy(1,6),-NV(6),0,0,0,dNxy(1,7),...
